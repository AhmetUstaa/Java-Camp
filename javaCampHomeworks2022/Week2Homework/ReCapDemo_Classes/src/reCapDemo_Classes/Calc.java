package reCapDemo_Classes;

public class Calc {
	public int add(int number1, int number2) {
		return number1 + number2;
	}

	public int sub(int number1, int number2) {
		return number1 - number2;
	}

	public int mul(int number1, int number2) {
		return number1 * number2;
	}

	public int div(int number1, int number2) {
		return number1 / number2;
	}

}
